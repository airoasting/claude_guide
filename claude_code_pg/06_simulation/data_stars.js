function generateStardust() {
  const data = [];
  let seed = 42;
  function rand() {
    seed = (seed * 1664525 + 1013904223) & 0xffffffff;
    return (seed >>> 0) / 0xffffffff;
  }
  // Milky Way band regions for denser coverage
  const regions = [
    {ra: 270, dec: -25, spread: 40},  // Sagittarius
    {ra: 300, dec: 40, spread: 30},   // Cygnus
    {ra: 15,  dec: 60, spread: 25},   // Cassiopeia
    {ra: 50,  dec: 50, spread: 20},   // Perseus
    {ra: 85,  dec: 0,  spread: 25},   // Orion
    {ra: 190, dec: 10, spread: 35},   // Virgo area
    {ra: 240, dec: 50, spread: 20},   // general coverage
    {ra: 130, dec: 40, spread: 30},   // general coverage
  ];
  for (let i = 0; i < 2500; i++) {
    if (i % 3 === 0) {
      // Milky Way band star
      const r = regions[Math.floor(rand() * regions.length)];
      const ra = (r.ra + (rand() - 0.5) * r.spread * 2 + 360) % 360;
      const dec = Math.max(-30, Math.min(89, r.dec + (rand() - 0.5) * r.spread));
      data.push([+ra.toFixed(2), +dec.toFixed(2)]);
    } else {
      // Random sky coverage
      const ra = rand() * 360;
      const dec = -30 + rand() * 120; // -30 to 90
      data.push([+ra.toFixed(2), +dec.toFixed(2)]);
    }
  }
  return data;
}
